#include "libft.h"

void	*ft_memset(void *dest, int src, size_t n)
{
	size_t	j;
	char	*p;

	j = 0;
	p = (char *)dest;
	while (j < n)
	{
		p[j] = src;
		j++;
	}
	return (dest);
}

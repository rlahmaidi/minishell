#include "libft.h"

int	ft_atoi(const char *str)
{
	int	j;
	int	c;
	int	f;

	j = 0;
	f = 1;
	c = 0;
	while (str[j] == '\r' || str[j] == '\v' || str[j] == '\b'
		|| str[j] == '\f' || str[j] == '\t'
		|| str[j] == '\n' || str[j] == ' ')
		j++;
	if (str[j] == '+' && str[j + 1] != '-')
		j++;
	if (str[j] == '-')
	{
		f = -1;
		j++;
	}
	while (str[j] >= '0' && str[j] <= '9')
	{
		c = c * 10 + str[j] - '0';
		j++;
	}
	return (c * f);
}

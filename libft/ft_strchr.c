#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	j;

	j = 0;
	while (s[j] != c && s[j] != '\0')
	{
		j++;
	}
	if (s[j] == c)
		return ((char *)s + j);
	return (0);
}
